package com.chatbot.exception;

/**
 * ChatbotException
 *
 * @author Fred
 * @since 2025/8/10
 */
public class ChatbotException extends RuntimeException {

	public ChatbotException() {
		super();
	}

	public ChatbotException(String message) {
		super(message);
	}

	public ChatbotException(String message, Throwable cause) {
		super(message, cause);
	}

}
